// 根据时间轴当前分钟，计算空域有效状态（应用所有 minute <= 当前 的事件）
// status 由区域类型派生（监控/禁飞/活动保障/管制等）
export function airspaceStateAt(airspace, minute) {
  let type = airspace.type
  for (const ev of events) {
    if (ev.effect && ev.effect.id === airspace.id && ev.minute <= minute) {
      type = ev.effect.type
    }
  }
  return { type, status: stateOfType(type), color: colorOfType(type) }
}

